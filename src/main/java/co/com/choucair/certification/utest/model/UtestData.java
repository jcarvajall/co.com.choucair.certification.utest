package co.com.choucair.certification.utest.model;

public class UtestData {

    private String strFirstName;
    private String strLastName;
    private String strEmail;
    private String strPassword;
    private String strPasswordConfirmed;

    public String getStrFirstName() {
        return strFirstName;
    }

    public void setStrFirstName(String strFirstName) {
        this.strFirstName = strFirstName;
    }

    public String getStrLastName() {
        return strLastName;
    }

    public void setStrLastName(String strLastName) {
        this.strLastName = strLastName;
    }

    public String getStrEmail() {
        return strEmail;
    }

    public void setStrEmail(String strEmail) {
        this.strEmail = strEmail;
    }

    public String getStrPassword() {
        return strPassword;
    }

    public void setStrPassword(String strPassword) {
        this.strPassword = strPassword;
    }

    public String getStrPasswordConfirmed() {
        return strPasswordConfirmed;
    }

    public void setStrPasswordConfirmed(String strPasswordConfirmed) {
        this.strPasswordConfirmed = strPasswordConfirmed;
    }
}
